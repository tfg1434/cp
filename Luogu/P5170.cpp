template<>
i64 MInt<0>::Mod = 998244353;
using Z = MInt<0>;

struct Num {
    Z f, g, h;
};

#define int int64_t
Z i2=499122177,i6=166374059;
Num calc(int n, int a, int b, int c) {
    Z ac = a/c, bc = b/c, m = (a*n+b)/c, n1 = n+1, n21 = n*2+1;
    Num d;
    if (a == 0) {
        d.f = bc*n1;
        d.g = n*n1*i2*bc;
        d.h = bc*bc*n1;
        return d;
    }
    if (a >= c || b >= c) {
        d.f = n*n1*i2*ac + n1*bc;
        d.g = n*n1*n21*i6*ac + n*n1*i2*bc;
        d.h = n*n1*n21*i6*ac*ac + n1*bc*bc + n*n1*ac*bc;
        auto e = calc(n, a%c, b%c, c);
        d.f += e.f;
        d.g += e.g;
        d.h += e.h + 2*bc*e.f + 2*ac*e.g;
        return d;
    }
    auto e = calc((a*n+b)/c-1,c,c-b-1,a);
    d.f = n*m-e.f;
    d.g = (m*n*n1 - e.h - e.f)*i2;
    d.h = n*m*(m+1) - 2*e.g - 2*e.f - d.f;
    return d;
}

void solve() {
    int n, a, b, c; cin >> n >> a >> b >> c;
    auto [f, g, h] = calc(n, a, b, c);
    cout << f << ' ' << h << ' ' << g << '\n';
}

