constexpr int p2(int x) { return (int)1 << x; }
constexpr int msk2(int x) { return p2(x)-1; }
constexpr int bits(int x) { return x == 0 ? 0 : 31-__builtin_clz(x); } // floor(log2(x)) 
 
constexpr i64 mod = 998244353;
using Z = MInt<mod>;
const int LOG = 29;
V<Z> po2 = {1};

void solve() {
    int n; cin >> n;
    while (po2.size() < max(LOG, n)+1) po2.pb(po2.back()*2);

    vi a(n);
    for (int i = 0; i < n; i++) cin >> a[i];

    Z ans = 0;
    for (int b = 0; b < LOG; b++) {
        V<bool> has_bit(n);
        for (int i = 0; i < n; i++)
            has_bit[i] = (bool)(a[i] & p2(b));

        Z sum_even = 0, sum_odd = 0, cur;
        for (int i = 0; i < n; i++) {
            cur += po2[max(0, i-1)];
            if (has_bit[i]) {
                swap(sum_even, sum_odd);
                sum_odd += cur;
                cur = 0;
            } 
            ans += po2[b] * po2[max(0, n-1-i-1)] * sum_odd;
        }
    }

    cout << ans << '\n';
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0); cout.tie(0);
    int tc; cin >> tc; while (tc--) solve();
    return 0;
}


