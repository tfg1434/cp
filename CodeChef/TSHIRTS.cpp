#define pct __builtin_popcount
constexpr int p2(int x) { return (int)1 << x; }
constexpr int msk2(int x) { return p2(x)-1; }
constexpr int bits(int x) { return x == 0 ? 0 : 31-__builtin_clz(x); } // floor(log2(x)) 

void solve() {
    int n; cin >> n;    
    string _; getline(cin, _);
    vector people_with_shirt(SHIRTS, vi());
    for (int i = 0; i < n; i++) {
        string s; getline(cin, s);
        int x;
        stringstream ss(s);
        while (ss >> x) {
            x--;
            people_with_shirt[x].pb(i);
        }
    }

    vector dp(SHIRTS+1, V<Z>(p2(n)));
    dp[0][0] = 1;
    // dp[i][msk] = ways if we assigned the first i t-shirts and the people in msk have been assigned a shirt
    for (int i = 0; i < SHIRTS; i++) {
        for (int msk = 0; msk < p2(n); msk++) {
            // don't assign the shirt i to anyone
            dp[i+1][msk] += dp[i][msk];
            for (int u : people_with_shirt[i]) if (!(msk & p2(u))){
                dp[i+1][msk|p2(u)] += dp[i][msk];
            }
        }
    }

    cout << dp[SHIRTS][p2(n)-1] << '\n';
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0); cout.tie(0);
    int tc; cin >> tc; while (tc--) solve();
    return 0;
}
