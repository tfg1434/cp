constexpr int P = 1000000007;
using Z = MInt<P>;

int nu(int p, int n) {
    int res = 0;
    while (n%p == 0) {
        res++;
        n /= p;
    }
    return res;
}

const int N = 1e5;
bool is_prime[N+1];
void sieve() {
    for (int i = 2; i <= N; i++) is_prime[i] = true;
    for (int i = 2; i <= N; i++) if (is_prime[i]) {
        for (int j = 2*i; j <= N; j += i) {
            is_prime[j] = false;
        }
    }
}

void solve() {
    int n; cin >> n;
    vi a(n+1);
    for (int i = 1; i <= n; i++) cin >> a[i];

    Z ways = 1;
    for (int i = 1; i <= n; i++) if (a[i] != -1) {
        for (int j = 2*i; j <= n; j += i) if (a[j] != -1) {
            if (a[j]%i != a[i]) ways = 0;
        }
    }

    for (int p = 2; p <= n; p++) if (is_prime[p]) {
        int alpha = 1;
        {
            int P = p;
            while (p < 320 && P*p <= n) {
                P *= p;
                alpha++;
            }
        }
        int beta = 0;
        for (int i = p; i <= n; i += p) if (a[i] != -1) {
            ckmax(beta, nu(p, i));
        }

        // if (p*p > n) {
            // if (p*p > n && a[p] == -1) assert(alpha-beta==1);
            // if (alpha-beta == 1) assert(p*p>n && a[p] == -1);
        // }
        ways *= power(p, alpha-beta);
    }

    cout << ways << '\n';
}

